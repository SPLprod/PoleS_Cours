let nb1 = 123;
let nb2 ="123";

console.log(nb1 == nb2); //Comparaoson des valeurs in-variable
console.log(nb1 === nb2); //Comparaison valeur & type variables

// L'opérateur de comparaisons === signifie "strictement égale à"
// L'opérateur de comparaisons == signifie "valeur égale à"

console.log(nb1 != nb2);
console.log(nb1 !== nb2);

// L'opérateur de comparaisons != signifie "valeur différente à"
// L'opérateur de comparaisons != signifie "valeur strictement différente à"